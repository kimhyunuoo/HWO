/* 
  Number
  자바스크립트에서 일반적인 숫자는 64비트 형식의 IEEE-754 표준
  기반 형태로 저장되는 자료형
  2진수, 8진수, 10진수, 16진수 다양한 진수 사용 
  16진수 ---> 0xFF
  8진수 ----> 0o71
  2진수 ----> 0b1101
  
  상수의 값으로는 
  
  [MAX / MIN]_VALUE
  [MAX / MIN]_SAFE_INTEGER
  [POSITIVE / NAGATIVE]_INFINTY, NaN

  대표 적인 string 기반 method 
  - Number.toString()
    문자열로 변환 하는 메서드
  - Number.toFixed() , Number.toPrecision()
    특정 자리수까지 제한하여 숫자 표현 ex) 0.4584 ---> 0.45
  - parseInt() / parseFloat()
    정수/ 실수로 반환시켜 준다. 
    */
